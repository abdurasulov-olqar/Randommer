        '''get available crypto types

        Args:
            api_key (str): api key

        Returns:
            list: list of types
        '''
        url = self.base_url + "Finance/CryptoAddress/Types"
        headers ={
            "X-Api-Key": api_key
        }
        response = requests.get(url, headers = headers)
